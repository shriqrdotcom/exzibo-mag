// ── Neon Restaurant Query Functions ──────────────────────────────────────────
// Used exclusively by /api/neon/restaurant/* routes.
// Uses @neondatabase/serverless HTTP driver directly — no Supabase involved.

import { neon } from './pg-sql.js'
import { normalizeAndValidateSlug } from '../lib/slug-utils.js'

// Fields that must be serialised as JSON strings for JSONB columns
const JSONB_FIELDS = new Set([
  'social_links', 'plan_limits', 'images', 'table_numbers',
  'menu_filters', 'filters_enabled',
])

// ── Field boundary constants ──────────────────────────────────────────────────
//
// Three tiers of access:
//   PUBLIC_RESTAURANT_FIELDS  — returned to unauthenticated / public callers
//   OWNER_ADMIN_PROFILE_PATCH — owner/admin may update these profile fields
//   SUPERADMIN_PLATFORM_PATCH — only superadmin may update these platform fields
//
// Platform fields (plan, plan_limits, status, lifecycle dates, owner_id) are
// intentionally absent from OWNER_ADMIN_PROFILE_PATCH.

const PUBLIC_RESTAURANT_FIELDS = new Set([
  'id', 'uid', 'slug', 'name', 'logo',
  'description', 'phone', 'location', 'additional_info',
  'digital_menu_link', 'digital_service_bell',
  'social_links', 'rating', 'accent_color', 'currency',
  'chef_info', 'servant_info',
  'images', 'table_numbers',
  'menu_filters', 'filters_enabled',
  'tables', // computed by neonRowWithTables
])

// Owner/admin may update only profile/operational fields.
// Must NOT include plan, plan_limits, status, lifecycle dates, or owner_id.
export const OWNER_ADMIN_PROFILE_PATCH = new Set([
  'name', 'logo', 'logo_key',
  'description', 'phone', 'location', 'additional_info',
  'digital_menu_link', 'digital_service_bell',
  'social_links', 'rating',
  'accent_color', 'currency',
  'chef_info', 'servant_info',
  'images', 'table_numbers',
  'menu_filters', 'filters_enabled',
  'place', 'note', 'gst',
])

// Superadmin-only: lifecycle, billing/entitlement, and slug-change fields.
// slug is intentionally absent from OWNER_ADMIN_PROFILE_PATCH — only a
// superadmin may change a restaurant's slug via patchNeonRestaurantPlatform.
export const SUPERADMIN_PLATFORM_PATCH = new Set([
  'plan', 'plan_limits',
  'status',
  'is_deleted', 'deleted_at',
  'start_date', 'end_date',
  'slug',
])

// Full write allowlist — union of both tiers.
// Internal callers must pre-filter to the appropriate tier; this set is the
// last-resort guard inside patchNeonRestaurant to prevent arbitrary column writes.
const ALLOWED_PATCH = new Set([
  ...OWNER_ADMIN_PROFILE_PATCH,
  ...SUPERADMIN_PLATFORM_PATCH,
])

function getSql() {
  const url = process.env.DATABASE_URL
  if (!url) throw new Error('[neon-restaurants] DATABASE_URL is not set')
  return neon(url)
}

// ── Read ──────────────────────────────────────────────────────────────────────

export async function getNeonRestaurantById(id) {
  const sql = getSql()
  const rows = await sql`
    SELECT * FROM restaurants
    WHERE id = ${id} AND is_deleted = false
    LIMIT 1
  `
  return rows[0] ?? null
}

