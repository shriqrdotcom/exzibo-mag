// ── Create ────────────────────────────────────────────────────────────────────

export async function createNeonRestaurant(payload) {
  const { uid, slug, name } = payload
  if (!uid)  throw new Error('uid is required')
  if (!slug) throw new Error('slug is required')
  if (!name) throw new Error('name is required')

  const sql = getSql()

  // Uniqueness guards — run both checks in parallel
  const [[slugTaken], [uidTaken]] = await Promise.all([
    sql`SELECT id FROM restaurants WHERE slug = ${slug} LIMIT 1`,
    sql`SELECT id FROM restaurants WHERE uid  = ${uid}  LIMIT 1`,
  ])
  if (slugTaken) throw new Error(`Slug "${slug}" is already taken in Neon`)
  if (uidTaken)  throw new Error(`UID "${uid}" is already taken in Neon`)

  // Platform fields (id, plan, status, plan_limits) are ALWAYS server-controlled.
  // Caller-provided values for these fields are intentionally ignored:
  //   id         — always generated by the DB (gen_random_uuid()); prevents ID spoofing
  //   plan       — always 'STARTER'; billing changes go through the platform update path
  //   status     — always 'active'; lifecycle changes go through the platform update path
  //   plan_limits — always {}; entitlement changes go through the platform update path
  // owner_id is set by the API layer from the verified session — never trusted from caller.
  const rows = await sql`
    INSERT INTO restaurants (
      id, uid, slug, name, owner_id,
      status, plan, place, note,
      accent_color, currency,
      phone, gst, description, chef_info, servant_info,
      social_links, rating, location, additional_info,
      digital_menu_link, digital_service_bell,
      plan_limits, images, logo, table_numbers
    ) VALUES (
      gen_random_uuid(),
      ${uid},
      ${slug},
      ${name},
      ${payload.owner_id ?? null},
      'active',
      'STARTER',
      ${payload.place ?? null},
      ${payload.note ?? null},
      ${payload.accent_color ?? '#6366F1'},
      ${payload.currency ?? 'INR'},
      ${payload.phone ?? null},
      ${payload.gst ?? null},
      ${payload.description ?? null},
      ${payload.chef_info ?? null},
      ${payload.servant_info ?? null},
      ${JSON.stringify(payload.social_links ?? {})}::jsonb,
      ${payload.rating ?? null},
      ${payload.location ?? null},
      ${payload.additional_info ?? null},
      ${payload.digital_menu_link ?? null},
      ${payload.digital_service_bell ?? false},
      ${JSON.stringify({})}::jsonb,
      ${JSON.stringify(payload.images ?? [])}::jsonb,
      ${payload.logo ?? null},
      ${JSON.stringify(payload.table_numbers ?? [])}::jsonb
    )
    RETURNING *
  `
  return rows[0]
}

// ── List ──────────────────────────────────────────────────────────────────────

// Adds a computed "tables" field so Neon rows match the shape Supabase returns.
// Supabase stores a count string like "5"; Neon stores table_numbers as a JSONB
// array. We derive the count on the way out — no schema change needed.
export function neonRowWithTables(row) {
  if (!row) return row
  const count =
    Array.isArray(row.table_numbers) && row.table_numbers.length > 0
      ? String(row.table_numbers.length)
      : null
  return { ...row, tables: count }
}

// ── toPublicRestaurant ────────────────────────────────────────────────────────
// Returns only the fields safe for public/unauthenticated callers.
// Strips owner_id, plan, plan_limits, status, lifecycle dates, internal keys.
export function toPublicRestaurant(row) {
  if (!row) return row
  const withTables = neonRowWithTables(row)
  return Object.fromEntries(
    Object.entries(withTables).filter(([k]) => PUBLIC_RESTAURANT_FIELDS.has(k))
  )
}

// Returns active (non-deleted) restaurants ordered newest-first.
// Pass an ids array (UUIDs) to restrict results to specific restaurants —
// used by the authenticated path where get_my_restaurant_ids already scoped
// the allowed set. Omit ids (null/undefined) for the DISABLE_AUTH dev path.
export async function getNeonRestaurants(ids) {
  const sql = getSql()
  let rows
  if (Array.isArray(ids) && ids.length > 0) {
    rows = await sql`
      SELECT * FROM restaurants
      WHERE id = ANY(${ids}::uuid[]) AND is_deleted = false
      ORDER BY created_at DESC
    `
  } else {
    rows = await sql`
      SELECT * FROM restaurants
      WHERE is_deleted = false
      ORDER BY created_at DESC
    `
  }
  return rows.map(neonRowWithTables)
}

// ── Tier-scoped patch helpers ─────────────────────────────────────────────────

// Owner/admin profile update — silently strips platform/internal fields.
// Throws if the filtered patch is empty (caller sent only forbidden fields).
export async function patchNeonRestaurantProfile(id, patch) {
  const safePatch = Object.fromEntries(
    Object.entries(patch).filter(([k]) => OWNER_ADMIN_PROFILE_PATCH.has(k))
  )
  if (Object.keys(safePatch).length === 0)
    throw new Error('No valid profile fields provided. Platform fields (plan, status, etc.) may only be updated by a superadmin.')
  return patchNeonRestaurant(id, safePatch)
}

// Superadmin platform/lifecycle/billing update — strips profile fields.
// Throws if the filtered patch is empty.
export async function patchNeonRestaurantPlatform(id, patch) {
  const safePatch = Object.fromEntries(
    Object.entries(patch).filter(([k]) => SUPERADMIN_PLATFORM_PATCH.has(k))
  )
  if (Object.keys(safePatch).length === 0)
    throw new Error('No valid platform fields provided. For profile fields use the profile update endpoint.')
  return patchNeonRestaurant(id, safePatch)
}

// ── Update ────────────────────────────────────────────────────────────────────

export async function patchNeonRestaurant(id, patch) {
  const entries = Object.entries(patch).filter(([k]) => ALLOWED_PATCH.has(k))
  if (entries.length === 0) throw new Error('No valid patch fields provided')

  const sql = getSql()

  // Build parameterised SET clause dynamically
  const setClauses = entries.map(([k], i) => `"${k}" = $${i + 1}`)
  const values = entries.map(([k, v]) =>
    JSONB_FIELDS.has(k) ? JSON.stringify(v) : v
  )

  setClauses.push('"updated_at" = now()')
  values.push(id) // last param → WHERE id = $N

  const query = `
    UPDATE restaurants
    SET ${setClauses.join(', ')}
    WHERE id = $${values.length} AND is_deleted = false
    RETURNING *
  `

  // sql.query() returns the rows array directly (same shape as tagged-template calls)
  const rows = await sql.query(query, values)
  return rows?.[0] ?? null
}
